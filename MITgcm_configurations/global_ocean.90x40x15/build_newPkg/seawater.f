C     !ROUTINE: SW_ADTG
C     !INTERFACE:
      Real*8 FUNCTION SW_ADTG  (S,T,P)

C     !DESCRIPTION: \bv
C     *=============================================================*
C     | S/R  SW_ADTG
C     | o compute adiabatic temperature gradient as per UNESCO 1983 routines.
C     *=============================================================*
C
C     started:
C              Armin Koehl akoehl@ucsd.edu
C     \ev

C     !USES:
      IMPLICIT NONE

C     !INPUT/OUTPUT PARAMETERS:
      Real*8 S,T,P

C     !LOCAL VARIABLES:
      Real*8 a0,a1,a2,a3,b0,b1,c0,c1,c2,c3,d0,d1,e0,e1,e2
      Real*8 sref
CEOP

      sref = 35.D0
      a0 =  3.5803D-5
      a1 = +8.5258D-6
      a2 = -6.836D-8
      a3 =  6.6228D-10

      b0 = +1.8932D-6
      b1 = -4.2393D-8

      c0 = +1.8741D-8
      c1 = -6.7795D-10
      c2 = +8.733D-12
      c3 = -5.4481D-14

      d0 = -1.1351D-10
      d1 =  2.7759D-12

      e0 = -4.6206D-13
      e1 = +1.8676D-14
      e2 = -2.1687D-16

      SW_ADTG =      a0 + (a1 + (a2 + a3*T)*T)*T
     &     + (b0 + b1*T)*(S-sref)
     &     + ( (c0 + (c1 + (c2 + c3*T)*T)*T) + (d0 + d1*T)*(S-sref) )*P
     &     + (  e0 + (e1 + e2*T)*T )*P*P

      RETURN
      END
